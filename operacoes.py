# A função soma retorna a soma de dois numeros
def soma(x,y):
    return x+y

# A função subtracao retorna a subtração de dois números
def subtracao(x,y):
    return x-y

# A função mult retorna a multiplicação entre dois números
def mult(x,y):
    return x*y

# A função div retorna a divisão entre dois números
def div(x,y):
    return x/y